// Loops and Iteration Exercise here

for (let i = 0; i <= 10; i++) {
    console.log(i);
}

for (let i = 3; i <= 15; i += 2) {
    console.log(i);
}

for (let i = 0; i < 50; i += 5) {
    console.log(i);
}

let fruits = ["Apple", "Orange", "Pear", "Plum", "Banana"];

for (let i = 0; i < fruits.length; i++) {
    console.log(fruits[i]);
}

let presidents = ["Washington", "Adams", "Jefferson", "Madison", "Monroe"];

for (let i = 0; i < presidents.length; i++) {
    console.log(i + 1 + ": " + presidents[i]);
}
